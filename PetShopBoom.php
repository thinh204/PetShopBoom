        button.addEventListener('click', function(event) {
            var productId = this.getAttribute('data-product-id');
            var modalId;
            switch (productId) {
                case '86466':
                    modalId = 'productModal1';
                    break;
                case '86467':
                    modalId = 'productModal2';
                    break;
                case '86468':
                    modalId = 'productModal3';
                    break;
                case '86469':
                    modalId = 'productModal4';
                    break;
                default:
                    modalId = '';
            }
            if (modalId) {
                openModal(event, modalId);
            }
        });
    });
});
